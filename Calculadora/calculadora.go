package main

import "fmt"

func main() {

	//declaração de variáveis
	num1 := 100
	num2 := 20
	soma := num1 + num2
	subtracao := num1 - num2
	multiplicacao := num1 * num2
	divisao := num1 / num2

	fmt.Println("A soma dos dois números será: ", soma)
	fmt.Println("A subtração dos dois números será: ", subtracao)
	fmt.Println("A multiplicacao dos dois números será: ", multiplicacao)
	fmt.Println("A divisão dos dois números será: ", divisao)

}
